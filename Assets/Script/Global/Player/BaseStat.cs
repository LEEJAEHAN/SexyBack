
public class BaseStat
{
    public int Str;
    public int Int;
    public int Spd;
    public int Luck;

    public BaseStat()
    {
        Str = 0;
        Int = 0;
        Spd = 0;
        Luck = 0;
    }
    public BaseStat(int str, int intel, int spd, int luc)
    {
        Str = str;
        Int = intel;
        Spd = spd;
        Luck = luc;
    }

}